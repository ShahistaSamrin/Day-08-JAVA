/* [1, 2]
   [3, 4]   */
import java.util.*;
public class twodimensionalarray{
    public static void main(String args[]){
        //initilization of 2D array
        int a[][]={{1,2},{3,4}};
        //accessing 1D array in 2D array
        System.out.println(Arrays.toString(a[0]));
        System.out.println(Arrays.toString(a[1]));
    }
}